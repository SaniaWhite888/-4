fun main() {
    print("Введите тип пищи (суп, каша, картошка, мясо): ")
    val food = readLine()!!

    when (food) {
        "суп" -> println("Время приготовления: 20-30 минут")
        "каша" -> println("Время приготовления: 15-25 минут")
        "картошка" -> println("Время приготовления: 10-15 минут")
        "мясо" -> println("Время приготовления: 40-60 минут")
        else -> println("Неизвестный тип пищи")
    }
}